static const BYTE Rcon[11] = {
  0x8d,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36
};

#define AES_BLOCKLEN 16
#define AES_KEYLEN   32
#define AES_keyExpSize 240

typedef struct { BYTE RoundKey[AES_keyExpSize]; } AES_ctx;

static void AddRoundKey(BYTE round, BYTE state[4][4], const BYTE *RoundKey)
{
    for (BYTE i = 0; i < 4; ++i)
        for (BYTE j = 0; j < 4; ++j)
            state[i][j] ^= RoundKey[(round * 4 * 4) + (i * 4) + j];
}

static void SubBytes(BYTE state[4][4]) {
    for (BYTE i = 0; i < 4; ++i)
        for (BYTE j = 0; j < 4; ++j)
            state[i][j] = sbox[state[i][j]];
}

static void ShiftRows(BYTE state[4][4]) {
    BYTE temp;
    temp = state[0][1]; state[0][1] = state[1][1]; state[1][1] = state[2][1]; state[2][1] = state[3][1]; state[3][1] = temp;
    temp = state[0][2]; state[0][2] = state[2][2]; state[2][2] = temp;
    temp = state[1][2]; state[1][2] = state[3][2]; state[3][2] = temp;
    temp = state[0][3]; state[0][3] = state[3][3]; state[3][3] = state[2][3]; state[2][3] = state[1][3]; state[1][3] = temp;
}

static BYTE xtime(BYTE x) { return (x << 1) ^ ((x >> 7) * 0x1b); }

static void MixColumns(BYTE state[4][4]) {
    BYTE i, Tmp, Tm, t;
    for (i = 0; i < 4; ++i) {
        t   = state[i][0];
        Tmp = state[i][0] ^ state[i][1] ^ state[i][2] ^ state[i][3];
        Tm  = state[i][0] ^ state[i][1]; Tm = xtime(Tm); state[i][0] ^= Tm ^ Tmp;
        Tm  = state[i][1] ^ state[i][2]; Tm = xtime(Tm); state[i][1] ^= Tm ^ Tmp;
        Tm  = state[i][2] ^ state[i][3]; Tm = xtime(Tm); state[i][2] ^= Tm ^ Tmp;
        Tm  = state[i][3] ^ t;          Tm = xtime(Tm); state[i][3] ^= Tm ^ Tmp;
    }
}

static void KeyExpansion(BYTE *RoundKey, const BYTE *Key)
{
    BYTE i, j, k;
    BYTE tempa[4];
    for (i = 0; i < 8; i++) {
        RoundKey[(i * 4) + 0] = Key[(i * 4) + 0];
        RoundKey[(i * 4) + 1] = Key[(i * 4) + 1];
        RoundKey[(i * 4) + 2] = Key[(i * 4) + 2];
        RoundKey[(i * 4) + 3] = Key[(i * 4) + 3];
    }
    for (i = 8; i < 60; i++) {
        for (j = 0; j < 4; j++) tempa[j] = RoundKey[(i - 1) * 4 + j];
        if (i % 8 == 0) {
            k = tempa[0]; tempa[0] = tempa[1]; tempa[1] = tempa[2]; tempa[2] = tempa[3]; tempa[3] = k;
            tempa[0] = sbox[tempa[0]]; tempa[1] = sbox[tempa[1]]; tempa[2] = sbox[tempa[2]]; tempa[3] = sbox[tempa[3]];
            tempa[0] ^= Rcon[i / 8];
        } else if (i % 8 == 4) {
            tempa[0] = sbox[tempa[0]]; tempa[1] = sbox[tempa[1]]; tempa[2] = sbox[tempa[2]]; tempa[3] = sbox[tempa[3]];
        }
        RoundKey[i * 4 + 0] = RoundKey[(i - 8) * 4 + 0] ^ tempa[0];
        RoundKey[i * 4 + 1] = RoundKey[(i - 8) * 4 + 1] ^ tempa[1];
        RoundKey[i * 4 + 2] = RoundKey[(i - 8) * 4 + 2] ^ tempa[2];
        RoundKey[i * 4 + 3] = RoundKey[(i - 8) * 4 + 3] ^ tempa[3];
    }
}

static void Cipher(BYTE state[4][4], const BYTE *RoundKey)
{
    BYTE round = 0;
    AddRoundKey(0, state, RoundKey);
    for (round = 1; round < 14; ++round) {
        SubBytes(state);
        ShiftRows(state);
        MixColumns(state);
        AddRoundKey(round, state, RoundKey);
    }
    SubBytes(state);
    ShiftRows(state);
    AddRoundKey(14, state, RoundKey);
}

static void InvShiftRows(BYTE state[4][4]) {
    BYTE temp;
    temp = state[3][1]; state[3][1] = state[2][1]; state[2][1] = state[1][1]; state[1][1] = state[0][1]; state[0][1] = temp;
    temp = state[0][2]; state[0][2] = state[2][2]; state[2][2] = temp;
    temp = state[1][2]; state[1][2] = state[3][2]; state[3][2] = temp;
    temp = state[0][3]; state[0][3] = state[1][3]; state[1][3] = state[2][3]; state[2][3] = state[3][3]; state[3][3] = temp;
}

static void InvSubBytes(BYTE state[4][4]) {
    for (BYTE i = 0; i < 4; ++i)
        for (BYTE j = 0; j < 4; ++j)
            state[i][j] = rsbox[state[i][j]];
}

static BYTE Multiply(BYTE x, BYTE y) {
    BYTE r = 0;
    while (y) {
        if (y & 1) r ^= x;
        x = xtime(x);
        y >>= 1;
    }
    return r;
}

static void InvMixColumns(BYTE state[4][4]) {
    BYTE i, a, b, c, d;
    for (i = 0; i < 4; ++i) {
        a = state[i][0]; b = state[i][1]; c = state[i][2]; d = state[i][3];
        state[i][0] = Multiply(a, 0x0e) ^ Multiply(b, 0x0b) ^ Multiply(c, 0x0d) ^ Multiply(d, 0x09);
        state[i][1] = Multiply(a, 0x09) ^ Multiply(b, 0x0e) ^ Multiply(c, 0x0b) ^ Multiply(d, 0x0d);
        state[i][2] = Multiply(a, 0x0d) ^ Multiply(b, 0x09) ^ Multiply(c, 0x0e) ^ Multiply(d, 0x0b);
        state[i][3] = Multiply(a, 0x0b) ^ Multiply(b, 0x0d) ^ Multiply(c, 0x09) ^ Multiply(d, 0x0e);
    }
}

static void InvCipher(BYTE state[4][4], const BYTE *RoundKey)
{
    BYTE round;
    AddRoundKey(14, state, RoundKey);
    for (round = 13; round > 0; --round) {
        InvShiftRows(state);
        InvSubBytes(state);
        AddRoundKey(round, state, RoundKey);
        InvMixColumns(state);
    }
    InvShiftRows(state);
    InvSubBytes(state);
    AddRoundKey(0, state, RoundKey);
}

static void AES_init_ctx(AES_ctx *ctx, const BYTE *key)
{
    KeyExpansion(ctx->RoundKey, key);
}

static void AES_ECB_encrypt(const AES_ctx *ctx, BYTE *buf)
{
    BYTE state[4][4];
    for (BYTE i = 0; i < 4; i++)
        for (BYTE j = 0; j < 4; j++)
            state[i][j] = buf[i + 4 * j];
    Cipher(state, ctx->RoundKey);
    for (BYTE i = 0; i < 4; i++)
        for (BYTE j = 0; j < 4; j++)
            buf[i + 4 * j] = state[i][j];
}

static void AES_ECB_decrypt(const AES_ctx *ctx, BYTE *buf)
{
    BYTE state[4][4];
    for (BYTE i = 0; i < 4; i++)
        for (BYTE j = 0; j < 4; j++)
            state[i][j] = buf[i + 4 * j];
    InvCipher(state, ctx->RoundKey);
    for (BYTE i = 0; i < 4; i++)
        for (BYTE j = 0; j < 4; j++)
            buf[i + 4 * j] = state[i][j];
}

/* ============================================
 * CTR Mode
 * ============================================ */
static void IncrementCtr(BYTE ctr[16])
{
    for (int i = 15; i >= 0; i--) {
        if (++ctr[i]) break;
    }
}

/* ============================================
 * Public: AES-256-CTR Decrypt
 * ============================================ */
BOOL Aes256CtrDecrypt(const BYTE *key, const BYTE *iv, const BYTE *ciphertext,
                      SIZE_T len, BYTE *plaintext)
{
    AES_ctx ctx;
    BYTE counter[16];
    BYTE keystream[16];
    SIZE_T i, j;

    AES_init_ctx(&ctx, key);
    for (i = 0; i < 16; i++) counter[i] = iv[i];

    for (i = 0; i < len; i += 16) {
        for (j = 0; j < 16; j++) keystream[j] = counter[j];
        AES_ECB_encrypt(&ctx, keystream);
        for (j = 0; j < 16 && (i + j) < len; j++) {
            plaintext[i + j] = ciphertext[i + j] ^ keystream[j];
        }
        IncrementCtr(counter);
    }
    return TRUE;
}
